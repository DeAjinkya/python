a = 10
b = 2

# arithmetic operators
print("a + b = ",a+b)
print("a - b = ",a-b)
print("a x b = ",a*b)
print("a / b = ",a/b)
print("a % b = ",a % b)
print("a // b = ",a//b)
print("a ** b = ",a**b)

# conditional operators
print(a>4) 
print(a<5) # false
print(a>=4)
print(a<=10)

print(a==10) # true
print(a==4) # false

print(a!=10) # false not equal to 10

